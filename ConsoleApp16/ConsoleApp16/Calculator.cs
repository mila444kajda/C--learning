using System;

namespace ConsoleApp16
{
    class Calculator
    {
        public float Dodawanie(float x, float y) => x + y;
        public float Odejmowanie(float x, float y) => x - y;
        public float Mnozenie(float x, float y) => x * y;
        public float Dzielenie(float x, float y) => x / y;
    }
}
